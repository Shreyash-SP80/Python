# 1. Numeric Types
# Used to store numeric values.

#   a) int (Integer)
#     Whole numbers (positive or negative)
#     Unlimited precision

x = 10
y = -5
big_num = 12345678901234567890


#   b) float (Floating Point)
#     Decimal numbers
#     Can also use scientific notation

pi = 3.14159
temp = -12.5
sci_num = 2.5e4  # 25000.0


#   c) complex
#     Numbers with real and imaginary parts (j suffix)

z = 3 + 5j
print(z.real)  # 3.0
print(z.imag)  # 5.0
