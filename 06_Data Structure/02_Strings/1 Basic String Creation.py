# Strings:
#   Strings are sequences of characters enclosed in single, double, or triple quotes in Python. 
#   They are immutable, meaning once created, they cannot be changed (though you can create new strings from them).

# Single quotes
str1 = 'Hello World'
print(str1)    # Output: Hello World


# Double quotes
str2 = "Python Programming"
print(str2)    # Output: Python Programming


# Triple quotes for multi-line strings
str3 = """This is a
multi-line
string"""
print(str3)

# Output: 
# This is a
# multi-line
# string


# Escape characters
str4 = "He said, \"Python is awesome!\""
print(str4)     # Output: He said, "Python is awesome!"



