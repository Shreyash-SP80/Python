# What is a Variable?
#   A variable is a named location in memory used to store data. 
#   Think of it like a labeled box where you can put information to use later in your program.

# Variable Naming Rules
#   Python has specific rules for variable names:
#    - Must start with a letter (a-z, A-Z) or underscore (_)
#    - Can contain letters, numbers (0-9), and underscores
#    - Cannot be a Python keyword (like if, for, while, etc.)
#    - Case-sensitive (age, Age, and AGE are different variables)

# Variable Assignment Syntax
#  The basic syntax to create a variable is:
#      variable_name = value


# Assigning values to variables
name = "Shreyash"
age = 25
height = 5.7
is_student = True

print(name)        # Output: Shreyash
print(age)         # Output: 25
print(height)      # Output: 5.7
print(is_student)  # Output: True# Assigning values to variables


# Python is dynamically typed, meaning you don't need to declare variable types explicitly. 
# The type is inferred from the value you assign.

print(type(name))    # Output: <class 'str'>
print(type(age))     # Output: <class 'int'>
print(type(height))  # Output: <class 'float'>



