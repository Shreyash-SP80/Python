# User Input:
# - Python provides simple ways to get input from users. The most common method is using the built-in input() function.

# The input() function reads a line from the user (as a string).

# Syntax:
# user_input = input("Prompt message: ")
# Displays an optional prompt message.
# Returns the user's input as a string.

name = input("Enter your name: ")
print(f"Hello, {name}!")

# Output:
# Enter your name: Shreyash
# Hello, Shreyash!

