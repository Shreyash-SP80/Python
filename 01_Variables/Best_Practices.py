# Best Practices
#    Use descriptive names (user_age instead of ua)
#    Follow naming conventions:
#       - snake_case for variables and functions (user_name)
#       - PascalCase for class names (ClassName)
#       - UPPERCASE for constants (MAX_VALUE)
#       - Initialize variables before using them
#       - Avoid using Python keywords and built-in function names as variables

# Example Good vs Bad Variable Names

# Good variable names
user_age = 30
account_balance = 1000.50
is_active = True

# Bad variable names
a = 30          # Not descriptive
bal = 1000.50   # Too short
flg = True      # Unclear meaning